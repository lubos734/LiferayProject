package cz.lubos.portlet.preferences.basic;

/**
 * Basic crate for preferences 
 *
 */
public class PreferencesCrate {

	private String permissionGroups;
	
	private String permissionUsers;

	
	
	public String getPermissionGroups() {
		return permissionGroups;
	}

	public void setPermissionGroups(String permissionGroups) {
		this.permissionGroups = permissionGroups;
	}

	public String getPermissionUsers() {
		return permissionUsers;
	}

	public void setPermissionUsers(String permissionUsers) {
		this.permissionUsers = permissionUsers;
	}
	

	
	
}
